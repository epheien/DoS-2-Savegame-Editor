void About_Display(HWND hWnd)
{
	WCHAR*		pszAbout;
	DWORD_PTR	vl[4];

	vl[0] = VERSION_MAJOR;
	vl[1] = REVISION_MAJOR;
	vl[2] = VERSION_MINOR;
	vl[3] = REVISION_MINOR;

	if (FormatMessage(FORMAT_MESSAGE_ALLOCATE_BUFFER|FORMAT_MESSAGE_FROM_STRING|FORMAT_MESSAGE_ARGUMENT_ARRAY,Locale_GetText(TEXT_ABOUT),0,0,(WCHAR *)&pszAbout,1,(va_list *)&vl))
		{
		MessageBox(hWnd,pszAbout,Locale_GetText(TEXT_TITLE_INFO),MB_ICONINFORMATION|MB_OK);
		LocalFree(pszAbout);
		}

	return;
}
